// Function to get components by type
export function getComponents(type: ComponentType): Component[] {
  switch (type) {
    case "cpu":
      return cpus
    case "gpu":
      return gpus
    case "ram":
      return ram
    case "storage":
      return storage
    case "motherboard":
      return motherboards
    case "powerSupply":
      return powerSupplies
    case "case":
      return cases
    case "cooling":
      return cooling
    default:
      return []
  }
}